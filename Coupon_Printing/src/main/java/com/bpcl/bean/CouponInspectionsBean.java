package com.bpcl.bean;

public class CouponInspectionsBean {

	private String plantLocationCode;
	
	private String couponSize;
	
	private Integer quantity;
	
	private Integer lotCode;

	public String getPlantLocationCode() {
		return plantLocationCode;
	}

	public void setPlantLocationCode(String plantLocationCode) {
		this.plantLocationCode = plantLocationCode;
	}

	public String getCouponSize() {
		return couponSize;
	}

	public void setCouponSize(String couponSize) {
		this.couponSize = couponSize;
	}

	public Integer getQuantity() {
		return quantity;
	}

	public void setQuantity(Integer quantity) {
		this.quantity = quantity;
	}

	public Integer getLotCode() {
		return lotCode;
	}

	public void setLotCode(Integer lotCode) {
		this.lotCode = lotCode;
	}


	
	

}
